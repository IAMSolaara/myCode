package com.mentalabs;

public class museo{
    String id;
    String sala;
    String nome;
    int anno;
    String autore;
    public museo(String idOpera, String salaOpera, String nomeOpera, int annoOpera, String autoreOpera){
	this.id = idOpera;
	this.sala = salaOpera;
	this.nome = nomeOpera;
	this.anno = annoOpera;
	this.autore = autoreOpera;
    }
}
