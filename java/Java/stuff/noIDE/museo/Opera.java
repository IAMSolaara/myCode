package com.mentalabs;

public class Opera{
    public String id;
    public String sala;
    public String nome;
    public int anno;
    public String autore;
    public Opera(String idOpera, String salaOpera, String nomeOpera, int annoOpera, String autoreOpera){
	this.id = idOpera;
	this.sala = salaOpera;
	this.nome = nomeOpera;
	this.anno = annoOpera;
	this.autore = autoreOpera;
    }
}
